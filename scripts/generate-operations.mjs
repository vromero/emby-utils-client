// Generates src/generated/operations.ts from spec/openapi.json
// Each operation is captured as: { operationId, method, path, tag, summary,
// pathParams: string[], queryParams: [{name, type, required, description}],
// hasBody: boolean }
import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
// Spec lives at the repo root under `spec/openapi.json`.
const specPath = resolve(__dirname, "../spec/openapi.json");
const outPath = resolve(__dirname, "../src/generated/operations.ts");

const spec = JSON.parse(readFileSync(specPath, "utf8"));

/** Resolve OpenAPI `$ref` strings against the root spec. */
function resolveRef(node) {
  if (!node || typeof node !== "object") return node;
  if (node.$ref) {
    const parts = node.$ref.replace(/^#\//, "").split("/");
    let cur = spec;
    for (const part of parts) cur = cur[part];
    return resolveRef(cur);
  }
  return node;
}

function schemaToTypeString(schema) {
  if (!schema) return "any";
  schema = resolveRef(schema);
  if (schema.enum) return schema.enum.map((v) => JSON.stringify(v)).join(" | ");
  const t = schema.type;
  if (t === "integer" || t === "number") return "number";
  if (t === "boolean") return "boolean";
  if (t === "string") return "string";
  if (t === "array") {
    return `${schemaToTypeString(schema.items)}[]`;
  }
  if (t === "object" || schema.properties) return "Record<string, any>";
  return "any";
}

const operations = [];
const seenIds = new Set();

for (const [pathKey, pathItem] of Object.entries(spec.paths)) {
  const sharedParams = pathItem.parameters || [];
  for (const [method, op] of Object.entries(pathItem)) {
    if (
      method === "parameters" ||
      method === "summary" ||
      method === "description" ||
      typeof op !== "object"
    )
      continue;
    if (!op.operationId) continue;

    let operationId = op.operationId;
    // Disambiguate duplicate operationIds by appending path suffix
    if (seenIds.has(operationId)) {
      const seg = pathKey.split("/").filter(Boolean).join("_").replace(/[{}.]/g, "");
      operationId = `${operationId}_${seg}`;
    }
    seenIds.add(operationId);

    const allParams = [...sharedParams, ...(op.parameters || [])].map(resolveRef);
    const pathParams = [];
    const queryParams = [];
    const headerParams = [];
    for (const p of allParams) {
      const info = {
        name: p.name,
        required: !!p.required,
        description: p.description || "",
        type: schemaToTypeString(p.schema),
      };
      if (p.in === "path") pathParams.push(info);
      else if (p.in === "query") queryParams.push(info);
      else if (p.in === "header") headerParams.push(info);
    }

    const hasBody = !!op.requestBody;
    let bodyContentType = null;
    if (hasBody) {
      const content = op.requestBody.content || {};
      bodyContentType = Object.keys(content)[0] || "application/json";
    }

    operations.push({
      operationId,
      method: method.toUpperCase(),
      path: pathKey,
      tag: (op.tags && op.tags[0]) || "Misc",
      summary: op.summary || "",
      description: op.description || "",
      pathParams,
      queryParams,
      headerParams,
      hasBody,
      bodyContentType,
    });
  }
}

// Emit TypeScript
const header = `// AUTO-GENERATED by scripts/generate-operations.mjs - DO NOT EDIT.
// Regenerate with: npm run generate

export interface ParamSpec {
  name: string;
  required: boolean;
  description: string;
  type: string;
}

export interface OperationSpec {
  operationId: string;
  method: "GET" | "POST" | "PUT" | "DELETE" | "HEAD" | "OPTIONS" | "PATCH" | "TRACE";
  path: string;
  tag: string;
  summary: string;
  description: string;
  pathParams: ParamSpec[];
  queryParams: ParamSpec[];
  headerParams: ParamSpec[];
  hasBody: boolean;
  bodyContentType: string | null;
}

export const operations: Record<string, OperationSpec> = `;

const serialized = JSON.stringify(
  Object.fromEntries(operations.map((o) => [o.operationId, o])),
  null,
  2
);

mkdirSync(dirname(outPath), { recursive: true });
writeFileSync(outPath, header + serialized + ";\n");

console.log(`Generated ${operations.length} operations -> ${outPath}`);
